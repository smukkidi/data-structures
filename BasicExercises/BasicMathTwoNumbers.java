package BasicExercises;

public class BasicMathTwoNumbers{
  public static void main(String[] args){
    int a = 125, b = 24;

    int c = a + b; System.out.println(c);
    int d = a - b; System.out.println(d);
    int e = a * b; System.out.println(e);
    int f = a / b; System.out.println(f);
    int g = a % b; System.out.println(g);
    
  }
}